package vasic.web.programiranje.model;

public class Izvestaj {
	
	private String aerodromNaziv;
	private int brojLetova;
	private int brojProdatihKarata;
	private float ukupnaCenaKarata;
	
	public Izvestaj() {}
	
	public String getAerodromNaziv() {
		return aerodromNaziv;
	}
	public void setAerodromNaziv(String aerodromNaziv) {
		this.aerodromNaziv = aerodromNaziv;
	}
	public int getBrojLetova() {
		return brojLetova;
	}
	public void setBrojLetova(int brojLetova) {
		this.brojLetova = brojLetova;
	}
	public int getBrojProdatihKarata() {
		return brojProdatihKarata;
	}
	public void setBrojProdatihKarata(int brojProdatihKarata) {
		this.brojProdatihKarata = brojProdatihKarata;
	}
	public float getUkupnaCenaKarata() {
		return ukupnaCenaKarata;
	}
	public void setUkupnaCenaKarata(float ukupnaCenaKarata) {
		this.ukupnaCenaKarata = ukupnaCenaKarata;
	}
	
	
	
	
	
}
